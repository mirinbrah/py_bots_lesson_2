#Глобальная переменная
# Она объявлена вне всех функций и видна отовсюду.
# Использовать глобальные переменные лучше всего для констант (значений, которые не меняются).
LESSON_NUMBER = "2"


# --- 2. Определение функций ---

def create_greeting_message(name, age):
    """
    "Чистая" функция: она получает все данные через аргументы (name, age),
    обрабатывает их и возвращает результат с помощью 'return'.
    Она не зависит от глобальных переменных (кроме чтения констант).
    """
    #greeting_text - это локальная переменная. Она существует только здесь.
    greeting_text = f"Пользователю {name} ({age} лет) отправлено приветствие."
    return greeting_text


def print_separator(symbol, length):
    """
    Эта функция не возвращает значение (возвращает None по умолчанию).
    Ее задача - выполнить действие: напечатать разделитель на экран.
    """
    # 'separator_line' - тоже локальная переменная.
    separator_line = symbol * length
    print(separator_line)


def process_user(user_name):
    """
    Эта функция демонстрирует чтение глобальной переменной
    и вызов других функций.
    """
    print(f"Это материал урока под номером: {LESSON_NUMBER}")

    # Локальная переменная, доступная только в process_user
    status = "обработан"
    print(f"Статус для пользователя '{user_name}': {status}")

    # Вызываем другую функцию из этой
    print_separator("=", 20)


# --- 3. Главная функция (точка входа) ---

def main():
    """
    Основная функция, которая управляет логикой программы,
    вызывая другие, более мелкие функции.
    """
    print("Программа запущена.")
    print_separator("-", 20)

    # Вызываем функцию create_greeting_message и сохраняем ее результат в переменную
    message1 = create_greeting_message("Женя", 30)
    print(message1)

    # Можно сразу вывести результат, не сохраняя в переменную
    print(create_greeting_message("Саша", 25))

    # Вызываем функцию, которая выполняет действия
    process_user("Иван")

    print("Программа завершена.")


# --- 4. "Волшебная" конструкция ---
# Этот код выполнится, только если мы запускаем именно этот файл напрямую.
if __name__ == "__main__":
    main()